target = 'XMAS'

@timer
def part1(input: str):
  # iterate over all rows+cols. whenever we find X (target[0]), we branch out in all directions to see if we can find a complete target
  num_targets_found = 0
  grid = []
  start_positions = []
  i_row = 0
  for row in input.split('\n'):
    grid.append([])
    for i_col, char in enumerate(row):
      grid[i_row].append(char)
      if row[i_col] == target[0]:
        start_positions.append((i_row, i_col))
    i_row += 1

  for (i_row, i_col) in start_positions:
    num_targets_found += check_for_targets_part_1(grid, target, i_row, i_col)
  
  return num_targets_found

def check_for_targets_part_1(grid, target, r0, c0):
  num_found = 0
  num_rows = len(grid)
  # This assumes a square grid
  num_cols = len(grid[0])

  # Define the dr and dc for each direction
  deltas = {
    'right': (0, 1),
    'left': (0, -1),
    'up': (-1, 0),
    'down': (1, 0),
    'diag_DR': (1, 1),
    'diag_DL': (1, -1),
    'diag_UR': (-1, 1),
    'diag_UL': (-1, -1),
  }

  for directions, (dr, dc) in deltas.items():
    row_indices = [r0 + i*dr for i in range(len(target))]
    if not all(0 <= r < num_rows for r in row_indices):
      continue
    col_indices = [c0 + i*dc for i in range(len(target))]
    if not all(0 <= c < num_cols for c in col_indices):
      continue

    letters = [grid[r][c] for r, c in zip(row_indices, col_indices)]
    num_found += 1 if ''.join(letters) == target else 0
      

  return num_found

def check_for_targets_part_2(grid, target, i_row, i_col):
  num_rows = len(grid)
  num_cols = len(grid[0])
  if i_row < 1 or i_row >= num_rows - 1 or i_col < 1 or i_col >= num_cols - 1:
    return 0
  
  diag_1 = set([grid[i_row-1][i_col-1], grid[i_row+1][i_col+1]]) # top left to bottom right
  diag_2 = set([grid[i_row+1][i_col-1], grid[i_row-1][i_col+1]]) # bottom left to top right

  target_adjacents = set(['M','S'])
  if diag_1 == target_adjacents and diag_2 == target_adjacents:
    # print(f'Found @ {i_row, i_col}')
    return 1
  return 0

@timer
def part2(input):
  # iterate over all rows+cols. whenever we find 'A', we branch out on the diagonals to see if it matches the target surroundings
  num_targets_found = 0
  grid = []
  start_positions = []
  i_row = 0
  for row in input.split('\n'):
    grid.append([])
    for i_col, char in enumerate(row):
      grid[i_row].append(char)
      if row[i_col] == 'A':
        start_positions.append((i_row, i_col))
    i_row += 1

  for (i_row, i_col) in start_positions:
    num_targets_found += check_for_targets_part_2(grid, target, i_row, i_col)
  
  return num_targets_found

